package day10.Calculator;

public class com_acorn_pjt {

	// 더하기
	public static int Plus(int su1, int su2) {
		int result = su1 + su2;
		return result;
	}

	// 빼기
	public static int subtract(int su1, int su2) {
		int result = su1 - su2;
		return result;
	}

	// 곱하기
	public static int multiply(int su1, int su2) {
		int result = su1 * su2;
		return result;
	}

	// 나누기
	public static int divide(int su1, int su2) {
		int result = su1 / su2;
		return result;
	}

}
